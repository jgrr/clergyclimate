<p class="question">What's this all about?</p>

<p>As <a href="/index.php">the letter</a> says, climate change is a huge problem, humans are causing the problem, and religious leaders feel a moral, ethical, and theological obligation to help fix the problem. One way we feel religious leaders can help address the climate crisis is by speaking out as a group.<p>

<p class="question">I'm not a clergy member, can I still sign?</p>

<p>We encourage you to get involved with the climate change efforts of our coordinators&mdash;the <a href="http://ncse.com">National Center for Science Education</a>, <a href="http://interfaithpowerandlight.org">Interfaith Power and Light</a>, and <a href="http://sojo.net">Sojourners</a>&mdash;but this particular letter is specifically for clergy. Before we add a name to the public list, we confirm that the signer is really a clergy member, so non-clergy shouldn't try adding themselves to this letter. Contact us and we'll find other ways for you to get involved, though.</p>

<p class="question">Why isn't my denomination listed in the statistics for signers, the signer search page, or on the signup form?</p>

<p>There are a few possibilities. The denominations listed in the statistics and search page reflect the people who already signed. If no one from your denomination has signed, then they won't show up. The solution: recruit clergy from your denomination!</p>

<p>The denominations listed on the signup page are based on a few different sources, especially the list of major religious groups published at <a href="http://www.adherents.com/rel_USA.html">Adherents.com</a>, and surveys curated by the <a href="http://www.thearda.com/Archive/Browse_p.asp">Association of Religious Data Archives</a>. We listed the largest groups to keep the signup process simple, and to make it possible to easily understand the published statistics. If you believe we made an error, please contact us.</p>

<p class="question">I signed the letter and now my information is out of date or inaccurate. What do I do?</p>

<p>Contact us and we'll update your record.</p>

